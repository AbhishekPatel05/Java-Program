import java.util.*;
public class posVSneg {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);

        int num = in . nextInt();
        if (num >= 0){
            System.out.println("POSITIVE");
        } else{
            System.out.println("NEGATIVE");
        }


        in.close();
    }
    
}
